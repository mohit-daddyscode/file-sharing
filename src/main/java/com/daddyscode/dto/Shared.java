package com.daddyscode.dto;

public class Shared {
	int id;
	int fileid;
	String sharedwith;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFileid() {
		return fileid;
	}
	public void setFileid(int fileid) {
		this.fileid = fileid;
	}
	public String getSharedwith() {
		return sharedwith;
	}
	public void setSharedwith(String sharedwith) {
		this.sharedwith = sharedwith;
	}
	
}
